        private void f_AppenExpression(object sender, ExpressionEventArgs e)
        {
            Form form = base.FindForm();
            if ((form != null) && (form.ActiveControl != null))
            {
                if (form.ActiveControl.GetType().ToString() == "Qisi.Editor.Controls.SuperBox")
                {
                    ((SuperBox) form.ActiveControl).AppendF(e.Type);
                }
                else
                {
                    Control control = this.ActiveSuperBox(form.ActiveControl);
                    if (control != null)
                    {
                        ((SuperBox) control).AppendF(e.Type);
                    }
                }
            }
            if (sender.GetType().ToString() == "Qisi.Editor.Controls.FunctionLabel")
            {
                if ((((Control) sender).Parent != null) && (((Control) sender).Parent == this.FlowLayoutPanels[0]))
                {
                    foreach (Control control in this.flowLayoutPanel2.Controls)
                    {
                        if ((control.GetType().ToString() == "Qisi.Editor.Controls.FunctionLabel") && (((FunctionLabel) control).Ftype == e.Type))
                        {
                            this.flowLayoutPanel2.ScrollControlIntoView(control);
                        }
                    }
                }
                if (((((Control) sender).Parent != null) && (((Control) sender).Parent.GetType().ToString() == "System.Windows.Forms.FlowLayoutPanel")) && ((((Control) sender).Parent.Parent != null) && (((Control) sender).Parent.Parent.GetType().ToString() == "Qisi.General.Controls.PopupHost")))
                {
                    ((PopupHost) ((Control) sender).Parent.Parent).Close();
                }
            }
        }

